package arithmeticOperators;

public class Operators_ex {

	public static void main(String[] args) {
		
		
		int a =22;
		
		int b=10;
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);//divisor
		System.out.println(a%b);// remainder
		
	}

}
